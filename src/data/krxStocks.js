// 로컬 KRX 종목 검색
export const searchKrxStocks = (query, limit = 10) => {
  if (!query || query.length < 1) return []
  const q = query.trim().toLowerCase()
  return KRX_STOCKS.filter(stock =>
    stock.name.includes(query.trim()) ||
    stock.nameEn.toLowerCase().includes(q) ||
    stock.ticker.startsWith(q)
  ).slice(0, limit)
}
